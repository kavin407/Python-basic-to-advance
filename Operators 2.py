a=10
b=5
#Arithmetic Operators
print(a+b) # Addition
print(a-b) # Subtraction
print(a*b) # Multiplication
print(a/b) # Division
print(a%b) # Mode means the remainder after division
print(a**b)#It multiplies a by itself b times
print(a//b) #It also gives the remainder but it rounds down the value
#to the nearest integer

#Comparison Operators

#For comparison operators we will use the same values of a and b
#For comparison operators the output will be in boolean values that is
#  either true or false
print(a==b) #Equal to
print(a!=b) #Not equal to
print(a>b) #Greater than
print(a<b) #Less than
print(a>=b) #Greater than or equal to
print(a<=b) #Less than or equal to

#Logical Operators
#Logical operators are used to combine conditional statements
g= True
h= False
print(g and h) #Logical AND operator returns true if both statements are true
print(g or h) #Logical OR operator returns true if one of the statements is true    
print(not g) #Logical NOT operator returns false if the statement is true